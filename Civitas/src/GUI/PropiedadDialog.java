    private void rellenaPropiedades(Jugador jugador){
        //dato para la lista
        DefaultListModel propiedades = new DefaultListModel<>();
        
        //se completan los datos
        for (CasillaCalle propiedad: jugador.getPropiedades()){
            propiedades.addElement(propiedad.getNombre());
        }
        //se le dice a la lista cu√°les son esos datos
        listaPropiedades.setModel(propiedades);
    }

